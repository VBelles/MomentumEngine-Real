function onAltarDestroyed_breakable_box_01()
	wait(0.1)
	stopEntities()
	wait(1)
	blendInCamera("chrysalides_cam", 0.001, CAMERA_PRIORITY_GAMEPLAY, INTERPOLATOR_CUBIC_OUT)
	wait(2)
	blendInCamera("player_camera", 0.001, CAMERA_PRIORITY_GAMEPLAY, INTERPOLATOR_CUBIC_OUT)
	wait(0.5)
	resumeEntities()
end